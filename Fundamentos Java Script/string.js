const escola = "Cod3r"

console.log(escola.charAt(4)) // charAt nos mostra a letra localizada no indice colocado
console.log(escola.charCodeAt(3)) // charCodeAt nos mostra o valor do indice colcoado no unicode html
console.log(escola.indexOf('3')) // indexOf nos mostra onde o valor colocado esta localizado no indice 
console.log(escola.substring(1)) // iniciar do indice colocado

console.log(escola.replace(3,'e')) // substituir valores dentro do local selecionado

